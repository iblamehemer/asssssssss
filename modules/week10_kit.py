def _build_kit_zip() -> bytes:
    """Assemble all brand assets into an in-memory ZIP."""
    buf = io.BytesIO()
    with zipfile.ZipFile(buf, "w", zipfile.ZIP_DEFLATED) as zf:
        company = st.session_state.get("company", "Brand")

        # Brand manifest JSON
        manifest = {
            "meta": {
                "company":    company,
                "industry":   st.session_state.get("industry",""),
                "tone":       st.session_state.get("tone",""),
                "audience":   st.session_state.get("audience",""),
                "generated":  datetime.datetime.now().isoformat(),
            },
            "logo": {
                "selected_variant": st.session_state.get("selected_logo"),
            },
            "typography": st.session_state.get("fonts", [None])[st.session_state.get("selected_font") or 0],
            "slogans":    st.session_state.get("slogans"),
            "selected_slogan": (st.session_state.get("slogans") or [None])[st.session_state.get("selected_slogan") or 0],
            "color_palettes": st.session_state.get("palettes"),
            "selected_palette": (st.session_state.get("palettes") or [None])[st.session_state.get("selected_palette") or 0],
            "campaign":   st.session_state.get("campaign"),
            "translations": st.session_state.get("translations"),
            "feedback":   st.session_state.get("feedback"),
        }
        zf.writestr("brand_manifest.json", json.dumps(manifest, indent=2, ensure_ascii=False))

        # Campaign copy TXT
        campaign = st.session_state.get("campaign") or {}
        slogans  = st.session_state.get("slogans") or []
        sel_sl   = st.session_state.get("selected_slogan", 0)
        slogan   = slogans[sel_sl]["text"] if slogans and sel_sl is not None else ""

        copy_lines = [
            f"BRAND KIT — {company.upper()}",
            "=" * 50,
            f"Industry: {st.session_state.get('industry','')}",
            f"Tone: {st.session_state.get('tone','')}",
            f"Audience: {st.session_state.get('audience','')}",
            "",
            "SELECTED TAGLINE",
            f'  "{slogan}"',
            "",
            "CAMPAIGN COPY",
            f"  Caption: {campaign.get('caption','')}",
            f"  Best Time: {campaign.get('bestTime','')}",
            f"  Format: {campaign.get('type','')}",
            f"  Hashtags: {' '.join(campaign.get('hashtags',[]))}",
            "",
            "PREDICTED METRICS",
            f"  CTR: {campaign.get('ctr','')}",
            f"  ROI: {campaign.get('roi','')}",
            f"  Engagement: {campaign.get('engagement','')}",
        ]
        zf.writestr("campaign_copy.txt", "\n".join(copy_lines))

        # Colour palette CSV
        palettes = st.session_state.get("palettes") or []
        sel_pal  = st.session_state.get("selected_palette", 0)
        if palettes and sel_pal is not None:
            pal = palettes[sel_pal]
            palette_lines = ["name,hex,role"]
            roles = ["Primary","Secondary","Background","Neutral"]
            for i, c in enumerate(pal.get("colors",[])):
                palette_lines.append(f"{pal['name']},{c},{roles[i] if i < len(roles) else 'Accent'}")
            zf.writestr("colour_palette.csv", "\n".join(palette_lines))

        # Translations TXT
        translations = st.session_state.get("translations") or []
        if translations:
            trans_lines = [f"MULTILINGUAL SLOGANS — {company.upper()}", "=" * 50, f'English: "{slogan}"', ""]
            for t in translations:
                trans_lines.append(f"{t['flag']} {t['lang']}: \"{t['text']}\"")
            zf.writestr("multilingual_slogans.txt", "\n".join(trans_lines))

        # README
        readme = f"""# {company} Brand Kit
Generated by BrandMind AI — CRS AI Capstone 2025-26

## Contents
- brand_manifest.json   — Complete brand data (JSON)
- campaign_copy.txt     — Campaign captions, hashtags, metrics
- colour_palette.csv    — HEX colour codes with roles
- multilingual_slogans.txt — Translated taglines (5 languages)
- README.md             — This file

## Usage
1. Import brand_manifest.json into your design tools
2. Use campaign_copy.txt for social media scheduling
3. Apply colour_palette.csv in Figma / Adobe XD
4. Use multilingual_slogans.txt for international campaigns

Generated: {datetime.datetime.now().strftime('%Y-%m-%d %H:%M')}
"""
        zf.writestr("README.md", readme)
    return buf.getvalue()


